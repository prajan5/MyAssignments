package week1.day2;

public class ClassRoomIfElseStatements {

	public static void main(String[] args) {
	int mark= 45;
	if (mark<20) {
		System.out.println("Grade:C");
		}
	else if (mark<40) {
		System.out.println("Grade:B");
		}
	else if (mark<50) {
		System.out.println("Grade:A");
		}
	else {
		System.out.println("Grade:O");
		}

	}

}
